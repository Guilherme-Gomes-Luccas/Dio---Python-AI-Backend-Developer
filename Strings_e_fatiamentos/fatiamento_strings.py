nome = "Guilherme Arthur de Caravalho"

print(nome[0]) # Primeira letra

print(nome[-1]) # Pega a ultima letra

print(nome[:9]) # Vai até o caracter 9

print(nome[10:]) # Começa no caracter 10

print(nome[10:16]) # Começa no caracter 10 e termina no 16

print(nome[10:16:2]) # Começa no caracter 10 e termina no 16 indo de 2 em 2

print(nome[:]) # Faz uma copia da string

print(nome[:: -1]) # Faz uma copia da string e depois a espelha